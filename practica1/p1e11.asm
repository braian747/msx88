	ORG 1000H
CAD	DB "CADENA"
	DB 00H
CAR	DB "A"
RES	DW ?

	ORG 3000H
CONCAR:	MOV CX, 0
LAZO:	MOV AL,[BX]
	CMP AL,00H
	JZ FIN
	CMP AL,DL
	JNZ SIG
	MOV BYTE PTR [BX],58H ;58H="X" EN ASCII
	INC CX
SIG:	INC BX
	JMP LAZO
FIN:	RET

	ORG 2000H
	MOV DL,CAR
	MOV BX, OFFSET CAD
	CALL CONCAR
	MOV RES,CX
	HLT
	END
