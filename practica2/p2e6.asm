	ORG 1000H
CERO 	DB "CERO:"
UNO	DB "UNO"
DOS	DB "DOS"
TRES	DB "TRES"

;... 	... ...  	

NUEVE	DB "NUEVE"

	ORG 1500H
MSJ1	DB "INGRESE UN NUMERO DE UN DIGITO"
F_MSJ1	DB ?
MSJ2	DB "VUELVA A INGRESAR EL NUMERO CERO PARA FINALIZAR LA EJECUCION"
F_MSJ2	DB ?
NUM	DB ?

	ORG 3000H
IMPRIMIR:CMP NUM,30H
	JZ IMP0
	CMP NUM,31H
	JZ IMP1
	CMP NUM,32H
	JZ IMP2
	CMP NUM,33H
	JZ IMP3
;	... ...
	CMP NUM, 39H
	JZ IMP9
	JMP FIN

IMP0:	MOV BX, OFFSET CERO
	MOV AL, 4
	INT 7
	JMP FIN

IMP1:	MOV BX, OFFSET UNO
	MOV AL, 3
	INT 7
	JMP FIN	

IMP2:	MOV BX, OFFSET DOS
	MOV AL, 3
	INT 7
	JMP FIN

IMP3:	MOV BX, OFFSET TRES
	MOV AL, 4
	INT 7
	JMP FIN

;...	... ...


IMP9:	MOV BX, OFFSET NUEVE
	MOV AL, 5
	INT 7

FIN:	RET	

	ORG 2000H
LOOP:	MOV BX, OFFSET MSJ1
	MOV AL, OFFSET F_MSJ1-OFFSET MSJ1
	INT 7
	MOV BX, OFFSET NUM
	INT 6
NO_SALIR:CALL IMPRIMIR
	CMP NUM,30H
	JZ CONFIRMAR
	JMP LOOP
CONFIRMAR:MOV BX, OFFSET MSJ2
	MOV AL, OFFSET F_MSJ2-OFFSET MSJ2
	INT 7
	MOV BX, OFFSET NUM
	INT 6
	CMP NUM,30H
	JNZ NO_SALIR
	HLT
	END
	